public class BoardDrawer
{
    public void DrawBoard(bool[,] board)
    {
        Console.Clear();
        StringBuilder s = new StringBuilder();
        for (int y = 0; y < board.GetLength(1); y++)
        {
            for (int x = 0; x < board.GetLength(0); x++)
            {
                if (board[x, y])
                {
                    s.Append("|X|");
                }
                else
                {
                    s.Append("___");
                }
            }
            s.Append("\n");
        }
        Console.WriteLine(s.ToString());
    }
}
